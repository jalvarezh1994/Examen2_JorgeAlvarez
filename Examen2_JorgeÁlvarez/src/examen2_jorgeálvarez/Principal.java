    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            int pos = AlbumCancionesCb.getSelectedIndex();
            Albums.get(pos).getCanciones().add(new Cancion());
            int pos2 = Albums.get(pos).getCanciones().size() - 1;
            Albums.get(pos).getCanciones().get(pos2).setNombre(NombreCancionesTf.getText());
            Albums.get(pos).getCanciones().get(pos2).setArtista(ArtistaCancionesTf.getText());
            try {
                Albums.get(pos).getCanciones().get(pos2).setDuracion(Integer.parseInt(DuracionCancionestTf.getText()));
            } catch (Exception e) {
            }
            Albums.get(pos).getCanciones().get(pos2).setGenero(GeneroCancionesTf.getText());
            JOptionPane.showMessageDialog(this, "Agregado");
        } catch (Exception e) {
            Albums.clear();
            JOptionPane.showMessageDialog(this, "No hay albumes a√∫n");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

